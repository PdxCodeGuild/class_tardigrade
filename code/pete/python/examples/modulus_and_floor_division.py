apples = 48

print(f"""
Lisa has {apples} apples.  She sells them by the dozen and gives any leftover apples
to Pete.  How many dozens of apples does Lisa sell and how many leftovers does
she give to Pete?
""")

dozens = apples // 12
leftovers = apples % 12

print(f"""
Lisa sold {dozens}-dozen apples and gave {leftovers} leftover apples to Pete.
""")

